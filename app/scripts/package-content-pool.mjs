import { createHash } from 'node:crypto';
import { existsSync, mkdirSync, readFileSync, readdirSync, rmSync, statSync, writeFileSync } from 'node:fs';
import { dirname, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const appRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const poolRoot = resolve(appRoot, '../data/content_pool_v1');
const generatedRoot = join(appRoot, 'src/generated/content-pool-v1');
const publicRoot = join(appRoot, 'public/content-pool-v1');
const checkOnly = process.argv.includes('--check');
const runtimeFiles = [
  'manifest.json',
  'topics.json',
  'posts.json',
  'concepts.json',
  'claims.json',
  'suggested_questions.json',
  'source_assets.json',
];
const collectionNames = {
  'topics.json': 'topics',
  'posts.json': 'posts',
  'concepts.json': 'concepts',
  'claims.json': 'claims',
  'suggested_questions.json': 'suggestedQuestions',
  'source_assets.json': 'sourceAssets',
};

function fail(message) {
  throw new Error(`CONTENT_POOL_PACKAGE_FAILED: ${message}`);
}

function sha256(bytes) {
  return createHash('sha256').update(bytes).digest('hex');
}

function walk(root) {
  if (!existsSync(root)) return [];
  return readdirSync(root).flatMap((name) => {
    const path = join(root, name);
    return statSync(path).isDirectory() ? walk(path) : [path];
  });
}

function relativeFiles(root) {
  return walk(root).map((path) => relative(root, path).replaceAll('\\', '/')).sort();
}

function equalLists(left, right) {
  return left.length === right.length && left.every((value, index) => value === right[index]);
}

function readJson(filename) {
  try {
    return JSON.parse(readFileSync(join(poolRoot, filename), 'utf8'));
  } catch {
    fail(`${filename} is missing or invalid JSON`);
  }
}

const manifest = readJson('manifest.json');
if (manifest.contentPoolVersion !== 'pilot-v1-20260717') fail('unexpected contentPoolVersion');
if (!Array.isArray(manifest.fixedFilenames) || !manifest.bundleFileHashes) fail('manifest has no fixed artifact inventory');

const expectedSourceFiles = ['manifest.json', ...manifest.fixedFilenames].sort();
const actualSourceFiles = relativeFiles(poolRoot);
if (!equalLists(actualSourceFiles, expectedSourceFiles)) fail('source directory has missing or extra files');
for (const filename of manifest.fixedFilenames) {
  const bytes = readFileSync(join(poolRoot, filename));
  if (sha256(bytes) !== manifest.bundleFileHashes[filename]) fail(`${filename} failed immutable hash verification`);
}

const collections = {};
for (const filename of runtimeFiles.slice(1)) {
  const bytes = readFileSync(join(poolRoot, filename));
  if (sha256(bytes) !== manifest.artifactHashes[filename]) fail(`${filename} failed runtime hash verification`);
  collections[collectionNames[filename]] = JSON.parse(bytes.toString('utf8'));
}
for (const [name, records] of Object.entries(collections)) {
  if (!Array.isArray(records) || records.length !== manifest.counts[name]) fail(`${name} count mismatch`);
}
if (manifest.approvedCount !== collections.posts.length) fail('approved post count mismatch');

const ids = (records, field = 'id') => new Set(records.map((record) => record[field]));
const topicIds = ids(collections.topics);
const postIds = ids(collections.posts);
const conceptIds = ids(collections.concepts);
const claimIds = ids(collections.claims);
const questionIds = ids(collections.suggestedQuestions);
if (!equalLists(manifest.feedOrderPostIds, collections.posts.map((post) => post.id))) fail('feed order does not match posts');
if (collections.sourceAssets.some((asset) => !postIds.has(asset.postId))) fail('source asset has dangling post reference');
for (const post of collections.posts) {
  if (!topicIds.has(post.topicId)
      || post.conceptIds.some((id) => !conceptIds.has(id))
      || post.claimIds.some((id) => !claimIds.has(id))
      || post.suggestedQuestionIds.some((id) => !questionIds.has(id))) {
    fail(`${post.id} has dangling references`);
  }
}
for (const question of collections.suggestedQuestions) {
  if (!postIds.has(question.postId) || !topicIds.has(question.topicId)
      || question.targetConceptIds.some((id) => !conceptIds.has(id))
      || (question.targetClaimIds ?? []).some((id) => !claimIds.has(id))) {
    fail(`${question.id} has dangling references`);
  }
}

const indexSource = `// Generated by scripts/package-content-pool.mjs. Do not edit.\n`
  + `// Immutable source version: ${manifest.contentPoolVersion}\n`
  + `export const PACKAGED_CONTENT_POOL_VERSION = '${manifest.contentPoolVersion}' as const;\n`
  + `export const packagedContentPoolFiles = {\n`
  + runtimeFiles.map((filename) => `  '${filename}': ${JSON.stringify(readFileSync(join(poolRoot, filename), 'utf8'))},`).join('\n')
  + `\n} as const;\n\n`
  + `export const packagedContentPoolReader = {\n`
  + `  expectedVersion: PACKAGED_CONTENT_POOL_VERSION,\n`
  + `  async readText(filename: keyof typeof packagedContentPoolFiles): Promise<string> {\n`
  + `    return packagedContentPoolFiles[filename];\n`
  + `  },\n`
  + `} as const;\n`;

function expectedBytes(filename, root) {
  return filename === 'index.ts' && root === generatedRoot
    ? Buffer.from(indexSource)
    : readFileSync(join(poolRoot, filename));
}

function verifyOutput(root, files) {
  const actual = relativeFiles(root);
  if (!equalLists(actual, [...files].sort())) fail(`${relative(appRoot, root)} is stale`);
  for (const filename of files) {
    if (!readFileSync(join(root, filename)).equals(expectedBytes(filename, root))) fail(`${relative(appRoot, join(root, filename))} is stale`);
  }
}

if (checkOnly) {
  verifyOutput(generatedRoot, [...runtimeFiles, 'index.ts']);
  verifyOutput(publicRoot, runtimeFiles);
} else {
  for (const root of [generatedRoot, publicRoot]) {
    rmSync(root, { recursive: true, force: true });
    mkdirSync(root, { recursive: true });
  }
  for (const filename of runtimeFiles) {
    const bytes = readFileSync(join(poolRoot, filename));
    writeFileSync(join(generatedRoot, filename), bytes);
    writeFileSync(join(publicRoot, filename), bytes);
  }
  writeFileSync(join(generatedRoot, 'index.ts'), indexSource);
}

console.log(`Packaged ${collections.posts.length} posts from ${manifest.contentPoolVersion} (${checkOnly ? 'verified' : 'generated'}).`);
